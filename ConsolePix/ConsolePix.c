void updateConsolePart(int minx, int miny, int maxx, int maxy)
{
    const int w = m_nScreenWidth;
    const int h = m_nScreenHeight;

    if ((unsigned)w > 0x7FFFu || (unsigned)h > 0x7FFFu) return;
    if (!m_bufScreen) return;

    const int w1 = w - 1;
    const int h1 = h - 1;

    int x0 = minx; if (x0 < 0) x0 = 0; else if (x0 > w1) x0 = w1;
    int y0 = miny; if (y0 < 0) y0 = 0; else if (y0 > h1) y0 = h1;
    int x1 = maxx; if (x1 < 0) x1 = 0; else if (x1 > w1) x1 = w1;
    int y1 = maxy; if (y1 < 0) y1 = 0; else if (y1 > h1) y1 = h1;

    if (x0 > x1 || y0 > y1) return;
    SMALL_RECT region = { (SHORT)x0, (SHORT)y0, (SHORT)x1, (SHORT)y1 };
    const COORD bufferSize  = { (SHORT)w, (SHORT)h };
    const COORD bufferCoord = { (SHORT)x0, (SHORT)y0 };

    WriteConsoleOutputW(m_hConsole, m_bufScreen, bufferSize, bufferCoord, &region);
}

